'''
✔ Создайте функцию-генератор.
✔ Функция генерирует N простых чисел,
начиная с числа 2.
✔ Для проверки числа на простоту используйте
правило: «число является простым, если делится
нацело только на единицу и на себя».
'''


def generate(n):
    num = 2
    prime_count = 0
    while prime_count < n:
        is_prime = True
        for i in range(2, num):
            if num % i == 0:
                is_prime = False
                break
        if is_prime:
            yield num
            prime_count += 1
        num += 1


primes = generate(10)
for prime in primes:
    print(prime)